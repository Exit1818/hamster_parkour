def str2list(string):
    depth = 0
    idx = 0
    splitchrs = ', )'
    cur = ''
    stack = []
    cur_list = []
    while idx < len(string):
        if string[idx] == '数':
            stack.append(cur_list)
            depth += 1
            idx += 3
            cur_list = []
        elif string[idx] == ',':
            cur_list.append(cur)
            cur = ''
            idx += 1
        elif string[idx] == ' ':
            idx += 1
        elif string[idx] == ')':
            cur_list.append(cur)
            cur = cur_list
            depth -= 1
            cur_list = stack.pop(depth)
            idx += 1
        elif string[idx] == '矢':
            while string[idx] != ')':
                cur += string[idx]
                idx += 1
            cur += string[idx]
            idx += 1
        else:
            while string[idx] not in splitchrs:
                cur += string[idx]
                idx += 1
    return cur

